# Définition de la fonction My_print_hello
def My_print_hello():
    """
    Cette fonction affiche 'Hello world' dans le terminal.
    Aucun argument n'est nécessaire.
    """
    # Corps de la fonction
    message = "Hello world"  # Création d'une variable 'message' avec la chaîne de caractères 'Hello world'
    print(message)           # Affichage du message dans le terminal

# Appel de la fonction
My_print_hello()