# Définition de la fonction My_print_name
def My_print_name(name):
    """
    Cette fonction prend un paramètre 'name' et affiche sa valeur.
    """
    # Affichage de la valeur du paramètre 'name'
    print("Nom:", name)

# Appels de la fonction avec différents paramètres
My_print_name("Alicia")
My_print_name("Pierre")
My_print_name("Luc")