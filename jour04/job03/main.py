# Définition de la fonction pour ajouter "Melon" à la liste de fruits
def ajouter_melon():
    # Création de la liste de fruits
    fruits = ["pomme", "cerise", "orange"]
    
    # Ajout de "Melon" à la fin de la liste
    fruits.append("Melon")
    
    # Affichage de la liste mise à jour
    print(fruits)

# Appel de la fonction pour effectuer l'ajout
ajouter_melon()