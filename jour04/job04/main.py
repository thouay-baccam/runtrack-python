# Définition de la fonction pour ajouter "Mangue" à la liste de fruits à l'index 2
def ajouter_mangue():
    # Création de la liste de fruits
    fruits = ["pomme", "cerise", "orange", "Melon"]
    
    # Remplacement de "orange" par "Mangue" à l'index 2 de la liste
    fruits[2] = "Mangue"
    
    # Affichage de la liste mise à jour
    print(fruits)

# Appel de la fonction pour effectuer la modification
ajouter_mangue()